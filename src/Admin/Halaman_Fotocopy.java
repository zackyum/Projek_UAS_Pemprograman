    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit terlebih dahulu!");
        return;
    }
    // Ambil data dari field input
    String layanan = jTextField1.getText().trim();
    String hargastr = jTextField2.getText().trim();
    if (layanan.isEmpty() || hargastr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
        return;
    }
    try {
        int harga = Integer.parseInt(hargastr);
        // Ambil ID dari baris terpilih di tabel
        int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
        // Update ke database
        Connection conn = koneksi.connect();
        String sql = "UPDATE jasa_fotocopy SET nama_layanan = ?, harga_per_lembar = ? WHERE jasa_id = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, layanan);
        stmt.setInt(2, harga);
        stmt.setInt(3, id);
        int rowsUpdated = stmt.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.");
            // Optional: refresh table
            tampilkanBarangKeTabel();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal diperbarui.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harga dan Stok harus berupa angka.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal edit: " + e.getMessage());
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

